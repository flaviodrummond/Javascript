/* Faça um programa que compare um número com 10 e imprime na tela 
"o número é maior, o número é menor o número é igual" */

const number = 19 // Criamos um número
if (number > 10) { // Iniciamos as comparações, fazendo a pergunta se este número é maior que 10
    console.log('Este número é Maior que 10')
} else if (number < 10) { // usamos o ELSE IF para perguntar se este número é menor que 10
    console.log('Este número é Menor que 10')
} else { // o Else para perguntar se o número é igual ao 10
    console.log('Este número é Igual a 10')
}

/* Usamos o método Else If para fazer essa comparação e imprimir na tela o resultado */