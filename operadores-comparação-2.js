/* Operadores de Comparação
> -> Maior
>= -> Maior ou igual
< -> Menor
<= -> Menor ou igual
*/

const numberOne = 25
const numberTwo = 20

console.log(numberOne > numberTwo)

const oneNumber = 60
const twoNumber = 80

console.log(oneNumber >= twoNumber)

const firstNumber = 120
const seccondNumber = 14

console.log(firstNumber < seccondNumber)

const umNumber = 40
const doisNumber = 40

console.log(umNumber <= doisNumber)