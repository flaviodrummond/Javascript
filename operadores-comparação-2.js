/* Operadores de Comparação
> -> Maior
>= -> Maior ou igual
< -> Menor
<= -> Menor ou igual
*/

const numberOne = 25
const numberTwo = 20

console.log(numberOne > numberTwo) // O 25 é mairo que o 20 ?

const oneNumber = 60
const twoNumber = 80

console.log(oneNumber >= twoNumber) // O 60 é maior ou igual a 80 ?

const firstNumber = 120
const seccondNumber = 14

console.log(firstNumber < seccondNumber) // O 14 é maior que 120 ?

const umNumber = 40
const doisNumber = 40

console.log(umNumber <= doisNumber) // O 40 é maior ou igual a 40 ?