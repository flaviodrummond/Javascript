/* Operadores de Atribuição
= -> Atribuição
+= -> Adição
-= -> Subtração
/= -> Divisão
*= -> Multiplicação
%= -> Resto
**= -> Exponencial
*/

let firstNumber = 20
firstNumber = firstNumber + 5 // usando a variável duas vezes para achar o valor
console.log(firstNumber)

let myNumber = 15
myNumber **=2 // As invés de fazer a variável duas vezes, posso simplificar usando as atribuições
console.log(myNumber)